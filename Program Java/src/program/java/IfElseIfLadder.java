package program.java;

public class IfElseIfLadder {
public static void main(String[] args) {
        int nilai = 85;

        if (nilai >= 90) {
            System.out.println("Grade A");
        } else if (nilai >= 80) {
            System.out.println("Grade B");
        } else if (nilai >= 70) {
            System.out.println("Grade C");
        } else {
            System.out.println("Grade D");
        }
    }
}

    

